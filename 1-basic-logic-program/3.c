//3.WAP to Find Area And Circumference of Circle

#include <stdio.h>

int main() {
float radius;
printf("3.WAP to Find Area And Circumference of Circle\n");
printf("Enter the radius of circle\n");
scanf("%f",&radius);
printf("\nArea of a Circle = %f",3.14*radius*radius);
printf("\nCircumference of a Circle = %f",2*3.14*radius);
getch();
return 0;

}
